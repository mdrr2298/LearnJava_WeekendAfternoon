package basic;

public class LearnEscapeSequence {
	
	// Escape sequence
	
	public static void main(String[] args) {
		
		System.out.println("This is a QA engineering class");
		System.out.println("This is a QA \tengineering class");
		System.out.println("This is a QA \nengineering class");
		System.out.println("This is a QA \'engineering\' class");
		
	}
	

	
	
	
	
	
	

}
