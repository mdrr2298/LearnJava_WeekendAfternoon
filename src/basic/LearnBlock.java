package basic;

public class LearnBlock {
	
	// Block
	
	{ // Block start point 
		
		// Block body
		
		System.out.println("Hello");
		
	} // Block end point
	
	public static void display() {
		// Method/Function (in other language)
		// Method is a block of code, giving name such as display (method) for recognition

		System.out.println("This is display method");
		
		{
			System.out.println("This is inside a block");
			
		}
	}
	
	public static void main(String[] args) {
		
		{ // Block start point 
			
			// Block body
			
			System.out.println("Hello");
			
		} // Block end point
		// static method/ variable can be called by class name
		
		LearnBlock.display();
	}
	

		
		
		
		
		
		
	
		
		
	




	
	
	
	
	
	
	
	

}
